/*

The structure of linked list is the following

struct Node
{
    int data;
    Node* next;
};

*/
class Solution
{
    public:
    void solve(vector<int>&data,Node* node){
        if(node==NULL) return ;
        Node* temp=node;
        while(temp!=NULL){
            data.push_back(temp->data);
            temp=temp->next;
        }
        return;
    }
    
    struct Node * mergeResult(Node *node1,Node *node2)
    {
        // your code goes here
        vector<int>data;
        solve(data,node1);
        solve(data,node2);
        sort(data.begin(),data.end(),greater<int>());
        Node* dummy=newNode(-1);
        Node* temp=dummy;
        for(int i=0;i<data.size();i++){
            temp->next= newNode(data[i]);
            temp=temp->next;
        }
        return dummy->next;
    }  
};


