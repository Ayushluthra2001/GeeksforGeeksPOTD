class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int a[], int n) 
    { 
        // Your code here
        int maxi=0;
        int start=0;
        int end=n-1;
        while(start<=end){
            while(a[start]>a[end]) end--;
            if(a[start]<=a[end]){
                maxi=max(maxi,end-start);
            }
            start++;
            end=n-1;
        }
        
        
       
        return maxi;
    }
};

